function ShowMenu(){
    var menu = document.getElementById("Menu-showhide");
        menu.style.display = "block";
    var bgmenu = document.getElementById("Bg-Menu");
        bgmenu.style.display = "block";
}
function hideMenu(){
    var menu = document.getElementById("Menu-showhide");
        menu.style.display = "none";
    var bgmenu = document.getElementById("Bg-Menu");
        bgmenu.style.display = "none";
}